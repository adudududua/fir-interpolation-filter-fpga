%=============================================================
% 文件名       : make_vs1053_ab_pcm_mem.m
% 功能简述     : 生成 VS1053 A/B 听感对比用 PCM ROM 数据。
%
% A 音频：
%   模拟低采样率 + 零阶保持 + 粗量化后的粗糙版本。
%
% B 音频：
%   平滑的原始音频版本。
%
% 数据格式：
%   8 kHz
%   16 bit signed PCM
%   mono
%   little-endian
%
% 输出文件：
%   audio_A_raw_s16le_mono_8k_8192.mem
%   audio_B_smooth_s16le_mono_8k_8192.mem
%
% 设计作者     : kafeizizi
% 创建日期     : 2026-06-21
%=============================================================

clear;
clc;

%=============================================================
% 1）基本参数
%=============================================================
fs = 8000;          % VS1053 播放用采样率
N  = 4096;          % 4096 samples，16bit mono 后是 8192 bytes
t  = (0:N-1) / fs;

%=============================================================
% 2）构造基础音频
%
% 用 440Hz + 660Hz 组成一个比较容易听出来的片段，
% 再加一个慢包络，让声音不是完全死板的单音。
%=============================================================
x_base = 0.55 * sin(2*pi*440*t) + ...
         0.25 * sin(2*pi*660*t);

env = 0.65 + 0.35 * sin(2*pi*2*t);

x_base = x_base .* env;
x_base = x_base / max(abs(x_base)) * 0.75;

%=============================================================
% 3）B 音频：平滑版本
%=============================================================
x_B = x_base;

%=============================================================
% 4）A 音频：粗糙版本
%
% 做法：
%   先每 4 点取 1 点，相当于低采样率；
%   再重复 4 次，相当于零阶保持；
%   再做 4-bit 近似量化，增强粗糙听感。
%=============================================================
x_low = x_base(1:4:end);
x_zoh = repelem(x_low, 4);
x_zoh = x_zoh(1:N);

quant_level = 8;                 % 正负方向约 4-bit 量化感
x_A = round(x_zoh * quant_level) / quant_level;

x_A = x_A / max(abs(x_A)) * 0.75;

%=============================================================
% 5）写 wav，方便电脑试听确认
%=============================================================
audiowrite('audio_A_raw_s16le_mono_8k_test.wav',    x_A, fs);
audiowrite('audio_B_smooth_s16le_mono_8k_test.wav', x_B, fs);

%=============================================================
% 6）量化成 16bit signed PCM
%=============================================================
x_A_i16 = int16(round(x_A * 32767));
x_B_i16 = int16(round(x_B * 32767));

%=============================================================
% 7）写 mem 文件
%
% 每个采样点 16bit，小端格式：
%   低 8 位先发送；
%   高 8 位后发送。
%=============================================================
write_pcm_mem('audio_A_raw_s16le_mono_8k_8192.mem',    x_A_i16);
write_pcm_mem('audio_B_smooth_s16le_mono_8k_8192.mem', x_B_i16);

fprintf('已生成 audio_A_raw_s16le_mono_8k_8192.mem\n');
fprintf('已生成 audio_B_smooth_s16le_mono_8k_8192.mem\n');
fprintf('已生成 audio_A_raw_s16le_mono_8k_test.wav\n');
fprintf('已生成 audio_B_smooth_s16le_mono_8k_test.wav\n');

%=============================================================
% 本地函数：写 16bit PCM 小端 mem
%=============================================================
function write_pcm_mem(filename, x_i16)

    fid = fopen(filename, 'w');

    if fid == -1
        error('无法创建文件：%s', filename);
    end

    for i = 1:length(x_i16)
        val_u16 = typecast(x_i16(i), 'uint16');

        low_byte  = bitand(val_u16, 255);
        high_byte = bitshift(val_u16, -8);

        fprintf(fid, '%02X\n', low_byte);
        fprintf(fid, '%02X\n', high_byte);
    end

    fclose(fid);
end

