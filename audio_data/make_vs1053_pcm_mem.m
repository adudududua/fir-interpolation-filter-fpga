%=============================================================
% 文件名       : make_vs1053_pcm_mem.m
% 功能简述     : 生成 VS1053 WAV/PCM 播放测试用 8bit ROM 数据文件。
%
% 数据格式     :
%                8 kHz 采样率
%                16 bit signed PCM
%                单声道 mono
%                小端格式 little-endian·
%
% 输出文件     :
%                audio_pcm_s16le_mono_8k_8192.mem
%                audio_pcm_s16le_mono_8k_test.wav
%
% 设计作者     : kafeizizi
% 创建日期     : 2026-06-21
%=============================================================

clear;
clc;

%=============================================================
% 1）基本参数
%=============================================================
fs = 8000;              % 采样率 8 kHz
N  = 4096;              % 4096 个采样点，约 0.512 秒
t  = (0:N-1) / fs;

%=============================================================
% 2）构造一个不刺耳的测试音频
%
% 这里不是单纯一个高频正弦，而是两个较低频率叠加，
% 再乘一个包络，让耳机里听起来更像“音频片段”。
%=============================================================
x1 = 0.55 * sin(2*pi*440*t);    % 440 Hz
x2 = 0.25 * sin(2*pi*660*t);    % 660 Hz

% 慢包络，避免突兀
env = 0.65 + 0.35 * sin(2*pi*2*t);

x = (x1 + x2) .* env;

% 防止溢出，归一化到 80% 满幅
x = x / max(abs(x)) * 0.80;

%=============================================================
% 3）量化为 16 bit signed PCM
%=============================================================
x_i16 = int16(round(x * 32767));

%=============================================================
% 4）写出 wav 文件，方便电脑试听
%=============================================================
wav_file = 'audio_pcm_s16le_mono_8k_test.wav';
audiowrite(wav_file, double(x_i16) / 32768, fs);

%=============================================================
% 5）写出 mem 文件
%
% VS1053 接收 WAV PCM 数据时，16bit PCM 数据采用小端格式：
%   低 8 位先发送
%   高 8 位后发送
%
% Verilog ROM 每个地址存 1 byte，所以每个采样点写两行。
%=============================================================
mem_file = 'audio_pcm_s16le_mono_8k_8192.mem';

fid = fopen(mem_file, 'w');

if fid == -1
    error('无法创建 mem 文件，请检查当前目录写入权限。');
end

for i = 1:N
    val_u16 = typecast(x_i16(i), 'uint16');

    low_byte  = bitand(val_u16, 255);
    high_byte = bitshift(val_u16, -8);

    fprintf(fid, '%02X\n', low_byte);
    fprintf(fid, '%02X\n', high_byte);
end

fclose(fid);

fprintf('已生成：%s\n', mem_file);
fprintf('已生成：%s\n', wav_file);
fprintf('PCM 数据字节数：%d bytes\n', N * 2);
