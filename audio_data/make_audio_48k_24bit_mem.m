
%=============================================================
% 文件名       : make_audio_48k_24bit_mem.m
% 脚本名       : make_audio_48k_24bit_mem
% 功能简述     : 生成 VS1053 听感辅助演示用 48kHz / 24bit
%                signed 音频 ROM 数据文件。
%
%                本脚本用于生成符合赛题输入规格的原始音频采样数据。
%                生成的 .mem 文件将在 FPGA 中作为音频输入 ROM。
%
%                FPGA 后续基于该同一段输入音频实时生成 A/B 两路：
%
%                A 路：低质量保持重构效果。
%                      从同一 48kHz 输入音频中抽取部分采样，
%                      并在 FPGA 内部进行保持输出，
%                      用于模拟未经 FIR 插值滤波的粗糙重构效果。
%
%                B 路：FPGA FIR 插值平滑重构效果。
%                      FPGA 调用 interp4_top_symm_ce 模块，
%                      对同一输入音频构造的低速参考流进行 4x FIR 插值，
%                      用于展示 FIR 插值滤波后的平滑重构效果。
%
%                注意：
%                      本脚本只生成原始输入音频 ROM，
%                      不预先生成插值前 / 插值后的 A/B 结果。
%                      A/B 差异必须在 FPGA 内部实时产生。
%
% 当前默认配置：
%                  采样率      ：48kHz
%                  数据位宽    ：24bit signed
%                  采样点数    ：4096 点
%                  输出 mem 文件：audio_in_48k_24bit_4096.mem
%                  试听 wav 文件：audio_in_48k_preview.wav
%
% 设计作者     : kafeizizi
% 创建日期     : 2026-06-21
% 版本         : V2018.3
% 开发工具     : MATLAB
% 修订记录     :
%                2026-06-21：新增 48kHz / 24bit 音频 ROM 生成脚本。
%=============================================================

clear;
clc;
close all;

%=============================================================
% 1）基本参数
%=============================================================
fs = 48000;          % 48kHz，符合赛题输入采样率要求
N  = 4096;           % 4096 个采样点，约 85.3ms，FPGA 中循环播放
t  = (0:N-1) / fs;

%=============================================================
% 2）构造测试音频
%
% 这里采用多频正弦叠加，并加入慢包络。
% 频率选择：
%   440Hz  ：基音
%   880Hz  ：二倍频
%   1760Hz ：高频细节
%
% 这样 A/B 对比时，保持重构和 FIR 插值重构的听感差异
% 会比单一正弦更明显。
%=============================================================
x = 0.50 * sin(2*pi*440*t)  + ...
    0.28 * sin(2*pi*880*t)  + ...
    0.16 * sin(2*pi*1760*t);

% 加慢包络，避免声音完全死板
env = 0.70 + 0.30 * sin(2*pi*3*t);
x = x .* env;

% 归一化，避免削顶
x = x / max(abs(x)) * 0.75;

%=============================================================
% 3）导出 wav 文件，方便电脑先试听
%=============================================================
audiowrite('audio_in_48k_preview.wav', x, fs);

%=============================================================
% 4）量化成 24bit signed
%
% 24bit signed 范围：
%   -2^23 ~ 2^23 - 1
%=============================================================
x_i24 = round(x * (2^23 - 1));

%=============================================================
% 5）写出 24bit mem 文件
%
% 每行一个 24bit 二进制补码数据，用 6 位十六进制表示。
%
% Verilog 中对应：
%   reg signed [23:0] audio_rom [0:4095];
%   $readmemh("audio_in_48k_24bit_4096.mem", audio_rom);
%=============================================================
fid = fopen('audio_in_48k_24bit_4096.mem', 'w');

if fid == -1
    error('无法创建 audio_in_48k_24bit_4096.mem，请检查当前目录权限。');
end

for i = 1:N
    v = x_i24(i);

    % 负数转成 24bit 二进制补码形式
    if v < 0
        v = v + 2^24;
    end

    fprintf(fid, '%06X\n', v);
end

fclose(fid);

fprintf('已生成：audio_in_48k_24bit_4096.mem\n');
fprintf('已生成：audio_in_48k_preview.wav\n');
fprintf('采样率：%d Hz\n', fs);
fprintf('采样点数：%d\n', N);
fprintf('数据位宽：24bit signed\n');

